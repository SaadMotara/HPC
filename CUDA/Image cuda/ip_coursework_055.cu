__global__ void detect_edges(float *s_in, float *s_out) {
  int i;
  int n_pixels = width * height;
  char *results;

  for(i=0;i<n_pixels;i++) {
    int x, y; // the pixel of interest
    int b, d, f, h; // the pixels adjacent to x,y used for the calculation
    int r; // the result of calculate
    
    y = i / width;
    x = i - (width * y);

    if (x == 0 || y == 0 || x == width - 1 || y == height - 1) {
      results[i] = 0;
    } else {
      b = i + width;
      d = i - 1;
      f = i + 1;
      h = i - width;

      r = (s_in[i] * 4) + (s_in[b] * -1) + (s_in[d] * -1) + (s_in[f] * -1)
          + (s_in[h] * -1);

      if (r > 0) { // if the result is positive this is an edge pixel
        s_out[i] = 255;
      } else {
        s_out[i] = 0;
      }
    }
  }
}

